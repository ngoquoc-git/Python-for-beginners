#Solution 1
firstVar = 2
secondVar = 5.5
thirdVar = ["apple", "banana", "peach"]     #list's items are wrapped in []

print(firstVar)
print(type(firstVar))       #print(type()) is used to show variables data type

print(secondVar)
print(type(secondVar))

print(thirdVar)
print(type(thirdVar))

#Solution 2
firstNewVar = float(firstVar)
secondNewVar = int(secondVar)

print(type(firstNewVar))
print(firstNewVar)

print(type(secondNewVar))
print(secondNewVar)

