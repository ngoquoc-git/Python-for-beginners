#Question 1
#Prompt the computer to generate 3 number from 1 to 100
#print the sum of them

#----------------------------------------

#Solution 1
import random

generator1 = random.randrange(1,100)
generator2 = random.randrange(1,100)
generator3 = random.randrange(1,100)

print(generator1)
print(generator2)
print(generator3)
print(generator1 + generator2 + generator3)
